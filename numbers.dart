// whole numbers (integers)
// decimals(double)

void main() {
  int num1 = 43;

  // double (decimal)
  double num2 = 43.5;
  var addition = num1 + num2;
  print(addition);

  var diff = num2 - num1;
  print(diff); 

  var product = num1 * num2;
  print(product);

  var division = num2 / num1;
  print(division);
}
